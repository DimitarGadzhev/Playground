// Problem 6. Print Numbers
// Write a program to print the numbers 1, 101 and 1001, each at a separate line.

using System;

class PrintNumbers
{
    static void Main()
    {
        Console.Title = "Problem 6. Print Numbers";
        Console.WriteLine("1 \n101 \n1001 \n");

        // "\n" is an escape character. Matches a new line, equal to \u000A.
    }
}
